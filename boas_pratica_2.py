# flake8: Ferramenta de linting para Python que verifica problemas de estilo, erros de sintaxe e padrões não recomendados.
# Ajuda a manter o código limpo e consistente, identificando possíveis bugs e más práticas.
# Exemplos de uso:
#   - Verificar um arquivo: flake8 script.py
#   - Personalizar regras: editar o arquivo .flake8 ou adicionar configurações no setup.cfg
#   - Pode ser integrado ao editor ou ao CI para garantir qualidade contínua do código.

# pipenv: Ferramenta para gerenciamento de dependências e ambientes virtuais em projetos Python.
# Facilita a instalação de pacotes e o controle de versões, criando arquivos Pipfile e Pipfile.lock.
# Exemplos de uso:
#   - Iniciar um novo ambiente: pipenv install
#   - Instalar um pacote: pipenv install requests
#   - Ativar o ambiente virtual: pipenv shell
#   - Os arquivos Pipfile e Pipfile.lock garantem que as dependências sejam reproduzíveis.

# poetry: Gerenciador de dependências e empacotamento para projetos Python.
# Permite criar, publicar e gerenciar pacotes, além de lidar com ambientes virtuais e arquivos pyproject.toml.
# Exemplos de uso:
#   - Iniciar um projeto: poetry init
#   - Instalar dependências: poetry add flask
#   - Ativar o ambiente virtual: poetry shell
#   - Publicar um pacote: poetry publish
#   - O arquivo pyproject.toml centraliza as configurações do projeto.

# isort: Utilitário para organizar automaticamente as importações em arquivos Python.
# Garante que as importações estejam ordenadas e agrupadas de forma consistente, melhorando a legibilidade do código.
# Exemplos de uso:
#   - Ordenar as importações de um arquivo: isort script.py
#   - Pode ser integrado ao editor ou ao CI para garantir padronização.

# black: Formatador de código Python que aplica um estilo consistente automaticamente.
# Facilita a padronização do código, tornando-o mais legível e reduzindo discussões sobre estilo.
# Exemplos de uso:
#   - Formatar um arquivo: black script.py
#   - Pode ser integrado ao editor ou ao CI para garantir que todo o código siga o mesmo padrão.